package com.sp.portal.brands;

import java.util.List;

import com.shine.boards.CorpBrand;

public class BrandsResult {
	private List<CorpBrand> brands;

	private Integer start;
	private Integer brandCount;
	private Integer totalBrands;

	public BrandsResult(List<CorpBrand> brands,Integer start, Integer brandCount, Integer totalBrands) {
		this.brands = brands;
		this.start = start;
		this.brandCount = brandCount;
		this.totalBrands = totalBrands;
	}

	public List<CorpBrand> getBrands() {
		return brands;
	}

	public Integer getStart() {
		return start;
	}

	public Integer getBrandCount() {
		return brandCount;
	}

	public Integer getTotalBrands() {
		return totalBrands;
	}
}
