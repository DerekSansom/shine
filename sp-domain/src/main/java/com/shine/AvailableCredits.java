package com.shine;

public class AvailableCredits {

	private int userId;
	private int advertCredits;
	private int boardCredits;
	
	
	public AvailableCredits(int user_id, int advert, int board) {
		this.userId = user_id;
		this.advertCredits = advert;
		this.boardCredits = board;
	}
	
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getAdvertCredits() {
		return advertCredits;
	}
	public void setAdvertCredits(int advertCredits) {
		this.advertCredits = advertCredits;
	}
	public int getBoardCredits() {
		return boardCredits;
	}
	public void setBoardCredits(int boardCredits) {
		this.boardCredits = boardCredits;
	}
	
	
}
