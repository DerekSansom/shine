package com.shine.objects;

import java.util.Date;

public abstract class Stone extends LocObject {

	int points;
	Date created;

	public Date getCreated() {
		return created;
	}

	public void setCreated(Date created) {
		this.created = created;
	}

	Stone(int id) {
		super(id);
	}

	Stone() {
	}

	public abstract int getPoints();

}
